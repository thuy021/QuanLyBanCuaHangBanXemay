	public boolean kiemTraDL() {
		String khung = txtSoKhung.getText().trim();
		String may = txtSoMay.getText().trim();
		String dt = txtDungTich.getText().trim();
		String sk = "^.*(?=.{7})(?=..*[0-9])(?=.*[A-Z]).*$";
		if (!(khung.length() > 0 && khung.matches(sk))) {
			JOptionPane.showMessageDialog(null,
					"Lỗi : Nhập Số khung sai định dạng!!!\n Số khung bao gồm 7 kí tự cả kí tự số và chữ in hoa.");
			return false;
		}
		String sm = "^.*(?=.{11})(?=..*[0-9])(?=.*[A-Z]).*$";
		if (!(may.length() > 0 && may.matches(sm))) {
			JOptionPane.showMessageDialog(null, "Lỗi : Nhập Số máy sai định dạng!!!\n Số máy bao gồm 11 kí tự cả kí tự số và chữ in hoa.");
			return false;
		}

		
		return true;
	}
	public void kiemTraDLSO() {
		String dt = txtDungTich.getText().trim();
		float dtich = Float.parseFloat(dt);
		String gia = txtGiaXe.getText().trim();
		float giaxe = Float.parseFloat(gia);
		String sl = txtSoLuong.getText().trim();
		int solg = Integer.parseInt(sl);
		if (dtich<=0) {
			JOptionPane.showMessageDialog(null,"Lỗi : Dung tích xe phải lớn hơn 0.");
		}
		if (giaxe<=0) {
			JOptionPane.showMessageDialog(null,"Lỗi : Giá xe phải lớn hơn 0.");
		}
		if (solg<=0) {
			JOptionPane.showMessageDialog(null,"Lỗi : Số lượng xe phải lớn hơn 0.");
		}		

	}


