    public ImageIcon ResizeImage(String imagePath, byte[] bit) {
        ImageIcon myImg = null;
        if (imagePath != null) {
            myImg = new ImageIcon(imagePath);
        } else {
            myImg = new ImageIcon(bit);
        }
        Image img = myImg.getImage();
        Image newImg = img.getScaledInstance(lblAnhXe.getWidth(), lblAnhXe.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }
